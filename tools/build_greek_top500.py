def strip_accents(ch):
    """Return the base Greek letter (lowercase) for a character."""
    nfkd = unicodedata.normalize("NFKD", ch)
    base = "".join(c for c in nfkd if unicodedata.category(c) != "Mn")
    return base.lower()


def decompose_word(word):
    """Break a Greek word into its pictograph letters."""
    letters = []
    seen_bases = set()
    for ch in word:
        base = strip_accents(ch)
        if base in GREEK_PICTOGRAPHS:
            info = GREEK_PICTOGRAPHS[base]
            # Don't duplicate consecutive identical letters in the summary
            key = info["name"]
            letters.append({
                "name": info["name"],
                "pic": info["pic"],
                "meaning": info["meaning"],
            })
    return letters


def letter_summary(letters):
    """Build a concise summary string like the Hebrew version."""
    parts = []
    for l in letters:
        short_meaning = l["meaning"].split(",")[0].strip()
        parts.append(f"{l['name']} ({l['pic']} {short_meaning})")
    return " + ".join(parts)


