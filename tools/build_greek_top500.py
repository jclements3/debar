def main():
    results = []
    seen = set()

    for rank, word, eng in TOP_500:
        # Deduplicate
        if word in seen:
            continue
        seen.add(word)

        letters = decompose_word(word)
        if not letters:
            continue

        summary = letter_summary(letters)

        results.append({
            "rank": rank,
            "grk": word,
            "eng": eng,
            "letters": letters,
            "letter_summary": summary,
        })

    with open(OUT_FILE, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)

    print(f"Wrote {len(results)} words to {OUT_FILE}")


if __name__ == "__main__":
    main()
